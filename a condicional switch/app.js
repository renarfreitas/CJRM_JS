// A condicional switch
const grade = 'f'

switch (grade) {
  case 'A':
    console.log(`Voce tirou um ${grade}`)
    break

  case 'B':
    console.log(`Voce tirou um ${grade}`)
    break

  case 'C':
    console.log(`Voce tirou um ${grade}`)
    break

  case 'D':
    console.log(`Voce tirou um ${grade}`)
    break

  case 'E':
    console.log(`Você tirou um ${grade}`)
    break
  default:
    console.log(`A nota ${grade}, é inválida!`)
}
